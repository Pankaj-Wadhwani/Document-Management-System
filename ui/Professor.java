    public void addSubjectInListSubject(){
        String subjects[];
       
        if(cSelected && semSelected && constructor){
            listSubject.removeAll();
            //subjects=subjectDatabase.getSubjects(jcbCourseName.getItemAt(0),Integer.parseInt((String)jcbSem.getSelectedItem()));
            
            subjects=subjectDatabase.getSubjects((String)jcbCourseName.getSelectedItem(),Integer.parseInt((String)jcbSem.getSelectedItem()));
            for (String subject : subjects) {
                listSubject.setModel( new javax.swing.AbstractListModel<String>(){
                   @Override
                   public int getSize() { return subjects.length; }
                   @Override
                   public String getElementAt(int i) { return subjects[i]; }
               });
            }
        }
    }
    public void addCourseInJCBCourse(){
        jcbCourseName.removeAllItems();
        String courses[]=courseDatabase.getAllCourses();
        for (String course : courses) {
            jcbCourseName.addItem(course);
        }
    }
    public void addSemInJCBSem(){
        jcbSem.removeAllItems();
        int sems[]=semesterDatabase.getSems();
        for(int i=0;i<sems.length;i++){
            jcbSem.addItem(sems[i]+"");
            System.out.println(sems[i]);
        }
    }
