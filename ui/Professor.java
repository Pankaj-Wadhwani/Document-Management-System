    private boolean checkFields(){
        if(txtProffesorName.getText().equals(null)){
            JOptionPane.showMessageDialog(null,"Enter professor name","Professor", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(jcbCourseName.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null,"Select course name","Professor", JOptionPane.ERROR_MESSAGE);
                return false;
        }
        else if(jcbSem.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null,"Select semester","Professor", JOptionPane.ERROR_MESSAGE);
                return false;
        }
        else if(listSubject.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null,"Select subject","Professor", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
        
    }
